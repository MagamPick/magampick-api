-- Phase 11: 공지사항 도메인 — announcements 생성
-- 의존: 선행 마이그레이션 없음 (독립 테이블)

CREATE TABLE announcements
(
    id           BIGSERIAL    PRIMARY KEY,
    tag          VARCHAR(20)  NOT NULL,
    pinned       BOOLEAN      NOT NULL DEFAULT FALSE,
    title        VARCHAR(200) NOT NULL,
    body         TEXT         NOT NULL,
    published_at DATE         NOT NULL,
    created_at   TIMESTAMP    NOT NULL DEFAULT NOW(),
    updated_at   TIMESTAMP    NOT NULL DEFAULT NOW(),
    CONSTRAINT chk_announcements_tag CHECK (tag IN ('NOTICE', 'EVENT', 'UPDATE'))
);

-- 정렬 조회용 복합 인덱스: pinned DESC, published_at DESC
CREATE INDEX idx_announcements_sort ON announcements (pinned DESC, published_at DESC, id DESC);

